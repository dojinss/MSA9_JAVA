package Day01;

public class Ex02_Print {
	//main
	public static void main(String[] args) {
		//기본 출력문
		// - print(), println()
		// - System.out 객체사용
		
		//print()
		System.out.print("안녕하세요.");
		
		//println()
		// : 지정한 문자열 출력후, 줄바꿈
		System.out.println("출력 후 줄바꿈");
		
		System.out.println("자바 첫 수업");
		System.out.println();
		System.out.println("그냥 쓰면 줄바꿈만");
		// 문자열 : "" 큰따옴표로 묶여있는 데이터
	}
}
